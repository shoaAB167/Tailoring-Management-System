            con = DBConnection.getConnection();
            String query;
 query = "INSERT INTO SHOAIB.SHIRT ( mobile,HEIGHT, CHEST, STOMACH, HIP, SHOULDER, HAND, CUP, COLLAR, ORDER_DATE, DELIVERY_DATE,quantity,price,type) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?)";

                ps=con.prepareStatement(query);
                ps.setString(1,NewCustomer.mobile_field.getText());
                ps.setString(2,(height.getText()));
                ps.setString(3,(chest.getText()));
                ps.setString(4, (stomach.getText()));
                ps.setString(5, (hip.getText()));
