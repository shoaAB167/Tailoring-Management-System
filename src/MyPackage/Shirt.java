                ps.setString(11, delivery_date.getText());
                ps.setString(12,(quantity1.getText()));
                ps.setString(13,(price1.getText()));
                ps.setString(14,String.valueOf(type.getSelectedItem()));
                
                boolean execute = ps.execute();
                JOptionPane.showMessageDialog(this,"Saved Successfully","Successful!!!",JOptionPane.INFORMATION_MESSAGE);
                
     ps.close();
     con.close();
